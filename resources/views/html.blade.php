    <h2>HTML</h2>
    <p>HTML is the markup language used to create web pages, structuring content with tags to define text, images, and other elements for display in a web browser. It works by using a series of tags, like <p> for a paragraph or <h1> for a main heading, to tell the browser how to present information. A standard HTML document includes a declaration, a <head> section for metadata, and a <body> section for the visible content.What HTML does
    Structures content: HTML organizes the content on a webpage, defining headings, paragraphs, lists, and more.
    Adds meaning: It assigns meaning to content, for example, distinguishing a heading from a paragraph.
    Incorporates other media: It allows for the inclusion of images, videos, and other multimedia content.
    Uses formatting tags: You can use specific tags to format text, such as making it bold (<b>), italic (<i>), or highlighted (<mark>), as shown in this example. How it works
    Tags: HTML uses tags, which are words surrounded by angle brackets (< >), to mark up content.
    Elements: Tags come in pairs, an opening tag and a closing tag, that enclose the content to form an element (e.g., <p>This is a paragraph.</p>).
    Basic structure: A standard HTML document has a basic structure:
    <!DOCTYPE html>: Declares the document type.
    <head>: Contains information about the page, like its title, which appears in the browser tab.
    <body>: Contains the visible content of the webpage that the user sees.
    rowsers: Web browsers like Chrome, Safari, or Firefox read HTML files and render them as the visual pages you see online.</p>
